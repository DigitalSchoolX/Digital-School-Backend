  // G·ª≠i email th√¥ng tin ki·ªÉm tra cho t·∫•t c·∫£ h·ªçc sinh trong l·ªõp
  async sendTestInfoToStudents(testInfoData, students) {
    try {
      console.log(
        `üìß Sending test info emails to ${students.length} students...`
      );
      const emailPromises = students.map(async (student) => {
        try {
          const subject = `üîî Th√¥ng b√°o ki·ªÉm tra: ${testInfoData.subject.name} - L·ªõp ${testInfoData.class.className}`;
          const htmlContent = this.createTestInfoEmailTemplate(
            testInfoData,
            student.name
          );
          const result = await emailService.sendEmail(
            student.email,
            subject,
            htmlContent
          );
          console.log(
            `‚úÖ Email sent to ${student.name} (${student.email}): ${result.messageId}`
          );
          return {
            studentId: student._id,
            studentName: student.name,
            email: student.email,
            success: true,
            messageId: result.messageId,
            message: result.message || "Email sent successfully",
          };
        } catch (error) {
          console.error(
            `‚ùå Failed to send email to ${student.name} (${student.email}):`,
            error.message
          );
          return {
            studentId: student._id,
            studentName: student.name,
            email: student.email,
            success: false,
            error: error.message,
          };
        }
      });
      const results = await Promise.allSettled(emailPromises);
      const emailResults = results.map((result) => {
        if (result.status === "fulfilled") {
          return result.value;
        } else {
          return {
            success: false,
            error: result.reason?.message || "Unknown error",
          };
        }
      });
      const successCount = emailResults.filter((r) => r.success).length;
      const failCount = emailResults.filter((r) => !r.success).length;
      console.log(
        `üìä Email sending completed: ${successCount} success, ${failCount} failed`
      );
      return {
        totalStudents: students.length,
        successCount,
        failCount,
        results: emailResults,
      };
    } catch (error) {
      console.error("‚ùå Error in sendTestInfoToStudents:", error);
      throw error;
    }
  }

